#Numeros (+, -, *, /, //, **, %)
num1 = 10
num2 = 20

print(num1 + num2)

#Concatenación (suma de las cadenas de texto)

nombre1 = "Luis"
nombre2 = "Dominguez"

print(nombre1 + " "+ nombre2)

#Operadores de comparación (<, >, <=, >=, ==)

print(num1 == num2)

#Operadores Logicos (or, and, not)

bool1 = True
bool2 = False

print(bool1 or bool2)